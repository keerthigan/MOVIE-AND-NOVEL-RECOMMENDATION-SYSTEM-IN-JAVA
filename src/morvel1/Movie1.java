package morvel1;

public class Movie1 {
	protected final String title;
	protected final int year;
	protected final String director;
	protected final double rating;
	protected final String genre;
	
	

	public Movie1(String title, int year, String director, double rating, String genre) {
		super();
		this.title = title;
		this.year = year;
		this.director = director;
		this.rating = rating;
		this.genre = genre;
	}
}